function changeImage() {
    var image = document.getElementById('semestre');
    if (image.src.match("S5.png")) {
        image.src = "../image/S6.png";
    }
    else if (image.src.match("S5.png")) {
        image.src = "../image/S6.png";
    }
    else {
        image.src = "../image/S5.png";
    }
}
